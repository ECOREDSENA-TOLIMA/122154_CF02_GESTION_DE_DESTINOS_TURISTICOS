      h2 1.3 Normativa

    p.mb-5(data-aos="fade-left") La regulación del turismo en Colombia se rige por una serie de normativas diseñadas para fomentar, desarrollar y regular la actividad turística de manera sostenible y competitiva. Estas leyes establecen los principios y mecanismos para la protección y aprovechamiento de los recursos turísticos, al tiempo que promueven la participación y coordinación entre el sector público y privado. Entre las disposiciones más relevantes se encuentran la Ley 300 de 1996, la Ley 1558 de 2012 y la Ley 2068 de 2020, junto con el Artículo 287 de la Constitución Política Colombiana, que otorgan autonomía a las entidades territoriales para la gestión del turismo en sus jurisdicciones.

